"""
Module: llm_judge.py
Description: LLM-as-a-Judge evaluation logic using Groq Llama-3. 
             Provides high-level factual validation of RAG outputs against 
             ground-truth reference answers.

Author: Gaurav Patil
Project: GraphRAG Inference Hackathon — TigerGraph 2026

Key Features:
    - PASS/FAIL binary classification for answer accuracy
    - Confidence level extraction from judge reasoning
    - Deterministic evaluation using temperature=0.0
"""

import logging
import os
import sys
from typing import Dict, Any
from groq import Groq

# Ensure project root is in path for config import
sys.path.insert(0, os.getcwd())
from config import GROQ_API_KEY, GROQ_MODEL

# Configure structured logging
logger = logging.getLogger(__name__)

# Initialize a dedicated Groq client for the evaluation service
_client = Groq(api_key=GROQ_API_KEY)

def extract_confidence(raw_response: str) -> str:
    """
    Extract confidence level from LLM judge raw response.
    
    Parses qualifier words to assign HIGH/MEDIUM/LOW confidence.
    Used to flag borderline PASS/FAIL decisions for review.
    
    Args:
        raw_response: Raw text output from LLM judge.
        
    Returns:
        str: "HIGH" | "MEDIUM" | "LOW" confidence label.
        
    Example:
        conf = extract_confidence("PASS - The answer is clearly correct.") # -> "HIGH"
    """
    response_lower: str = raw_response.lower()
    
    high_confidence: list[str] = ["clearly", "definitely", "exactly", "precisely",
                                 "correct", "accurate", "fully addresses"]
    low_confidence: list[str]  = ["partially", "somewhat", "vague", "incomplete",
                                 "borderline", "arguably", "could be better"]
    
    if any(word in response_lower for word in high_confidence):
        return "HIGH"
    elif any(word in response_lower for word in low_confidence):
        return "LOW"
    return "MEDIUM"

def llm_judge(query: str, answer: str, reference: str) -> Dict[str, Any]:
    """
    Judge Generated Answer against Reference Answer using LLM.
    
    Prompts the LLM to act as a strict evaluator, comparing the semantic
    and factual accuracy of the prediction against the ground truth.

    Args:
        query: The original medical question.
        answer: The answer generated by the RAG pipeline.
        reference: The ground truth reference answer.

    Returns:
        dict: Evaluation results including:
            - passed: Boolean verdict
            - raw_response: Full text from the judge LLM
            - confidence: HIGH/MEDIUM/LOW confidence score
            
    Example:
        # PASS example: "PASS - Answer correctly identifies Type 1 diabetes 
        #                as autoimmune with insulin deficiency"
        # FAIL example: "FAIL - Answer mentions incorrect treatment protocol"
    """
    logger.info(f"Judging answer quality for query: {query[:50]}...")
    
    prompt: str = f"""You are a strict medical QA evaluator.
    
Question: {query}
Reference Answer: {reference[:500]}
Generated Answer: {answer[:500]}

Evaluate if the Generated Answer is factually correct and addresses the question.
Reply with EXACTLY:
PASS - [One sentence reasoning if passed]
FAIL - [One sentence reasoning if failed]

Your verdict:"""

    try:
        # temperature=0.0 ensures deterministic verdicts for reproducibility
        # in hackathon scoring and benchmark audits.
        response = _client.chat.completions.create(
            model=GROQ_MODEL,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.0,
            max_tokens=80
        )
        
        raw: str = response.choices[0].message.content.strip()
        passed: bool = "PASS" in raw.upper()
        confidence: str = extract_confidence(raw)
        
        logger.info(f"Judge Verdict: {'PASS' if passed else 'FAIL'} | Confidence: {confidence}")
        
        return {
            "passed": passed,
            "raw_response": raw,
            "llm_judge_passed": passed,
            "confidence": confidence,
            "llm_judge_confidence": confidence
        }
        
    except Exception as e:
        logger.error(f"LLM Judge Error: {e}")
        return {
            "passed": False, 
            "raw_response": str(e), 
            "llm_judge_passed": False,
            "confidence": "ERROR",
            "llm_judge_confidence": "ERROR"
        }
